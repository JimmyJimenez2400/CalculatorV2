function addNumbers(Num1, Num2){
    let result = Num1 + Num2;
    return result;
}

addNumbers(1, 5);


function subtractNumbers(Num1, Num2){
    let result = Num1 - Num2;
    return result;
}

subtractNumbers(8, 5);


function multiplyNumbers(Num1, Num2){
    let result = Num1 * Num2;
    return result;
}

multiplyNumbers(4, 2);

function divideNumbers(Num1, Num2){
    let result = Num1 / Num2;
    return result;
}

divideNumbers(10, 5);